    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try{
            Insert insert = new Insert();
            EstoqueFrame ef = new EstoqueFrame();
            
            ef.setTxtAlimento(txtAlimento);
            ef.setTxtCodigo(txtCodigo);
            ef.setTxtFornecedor(txtFornecedor);
            ef.setTxtQuantidade(txtQuantidade);
            ef.setTxtTipo(txtTipo);
            ef.setTxtValidade(txtValidade);
            
            boolean result = insert.insertEstoque(ef);
            
            if (result){
                JOptionPane.showMessageDialog(this, "Item adicionado ao estoque com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE, OldMain.icon);
            }
        } catch(DbException e){
            JOptionPane.showMessageDialog(this, "Erro: "+e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        ResultSet rs = null;
        try {
            Select select = new Select();
            
            rs = select.select("estoque", Integer.parseInt(txtId.getText()));
            
            if (rs.next()){
                txtAlimento.setText(rs.getString("alimento"));
                txtCodigo.setText(rs.getString("codigo"));
                txtFornecedor.setText(rs.getString("fornecedor"));
                txtQuantidade.setText(rs.getString("quantidade"));
                txtTipo.setText(rs.getString("tipo")+"");
                txtValidade.setText(rs.getString("validadeM")+"/"+rs.getString("validadeA"));
            }
        } catch (SQLException | DbException e) {
            JOptionPane.showMessageDialog(this, "Erro: "+e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Erro: o campo 'Id' deve ser preenchido", "Erro", JOptionPane.ERROR_MESSAGE);
        } finally {
            DB.closeResultSet(rs);
        }
    }//GEN-LAST:event_btnSelectActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try{
            Update update = new Update();
            EstoqueFrame ef = new EstoqueFrame();
            
            ef.setTxtAlimento(txtAlimento);
            ef.setTxtCodigo(txtCodigo);
            ef.setTxtFornecedor(txtFornecedor);
            ef.setTxtQuantidade(txtQuantidade);
            ef.setTxtTipo(txtTipo);
            ef.setTxtValidade(txtValidade);
            ef.setTxtId(txtId);
            
            boolean result = update.updateEstoque(ef);
            
            if (result){
                JOptionPane.showMessageDialog(this, "Estoque de id = "+txtId.getText()+" atualizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE, OldMain.icon);
            }
        } catch(DbException e){
            JOptionPane.showMessageDialog(this, "Erro: "+e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Erro: o campo 'Id' deve ser preenchido", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

