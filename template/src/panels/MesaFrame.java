    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try{
            Insert insert = new Insert();
            MesaFrame mf = new MesaFrame();
            
            mf.setTxtNumero(txtNumero);
            mf.setTxtPedido(txtPedido);
            mf.setTxtVlrConta(txtVlrConta);
            
            boolean result = insert.insertMesa(mf);
            
            if (result){
                JOptionPane.showMessageDialog(this, "Item adicionado Ã  mesa com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE, OldMain.icon);
            }
        } catch(DbException e){
            JOptionPane.showMessageDialog(this, "Erro: "+e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        ResultSet rs = null; 
        try {
            Select select = new Select();
            
            rs = select.select("mesa", Integer.parseInt(txtId.getText()));
            
            if (rs.next()){
                txtNumero.setText(rs.getString("numero"));
                txtPedido.setText(rs.getString("pedido"));
                txtVlrConta.setText(rs.getString("vlrConta"));
            }
        } catch (SQLException | DbException e) {
            JOptionPane.showMessageDialog(this, "Erro: "+e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Erro: o campo 'Id' deve ser preenchido", "Erro", JOptionPane.ERROR_MESSAGE);
        } finally {
            DB.closeResultSet(rs);
        }
    }//GEN-LAST:event_btnSelectActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try{
            Update update = new Update();
            MesaFrame mf = new MesaFrame();
            
            mf.setTxtNumero(txtNumero);
            mf.setTxtPedido(txtPedido);
            mf.setTxtVlrConta(txtVlrConta);
            mf.setTxtId(txtId);
            
            boolean result = update.updateMesa(mf);
            
            if (result){
                JOptionPane.showMessageDialog(this, "Mesa de id = "+txtId.getText()+" atualizada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE, OldMain.icon);
            }
        } catch(DbException e){
            JOptionPane.showMessageDialog(this, "Erro: "+e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Erro: o campo 'Id' deve ser preenchido", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

